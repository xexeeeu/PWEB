var num1 = prompt('Digite o numero A:');
var num1Num = parseFloat(num1);

var num2 = prompt('Digite o numero B:');
var num2Num = parseFloat(num2);

alert('A + B = '+ (num1Num+num2Num));

alert('A - B = '+ (num1Num-num2Num));

alert('A * B = '+ (num1Num*num2Num));

alert('A % B = '+ (num1Num%num2Num));